// Operador de asignación

let a = 1;
console.log(a);


// Operadores de asignación compuestos: Son operadores que combinan una operación artitmética o lógica con una asignación, simplificando el codigo.

// Suma y asigna
a += 3; // a = a + 3
console.log(a);

// Resta y asigna
a -= 2; // a = a - 2
console.log(a);

// Potencia y asigna
a **= 4;
console.log(a); // a = a ** 4

/*
    *= Multiplica y asigna
    /= Divide y asigna
    %= Calcula el módulo y asigna
*/