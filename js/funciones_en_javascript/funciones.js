// Una función en JavaScript es un bloque de código reutilizable que realiza una tarea específica. Puede recibir parámetros (datos de entrada) y devolver un resultado.


// Llamada de la función antes de la declaración
miFuncion(6, 9);

// Declaración de la función
function miFuncion(a, b) {
    console.log("Suma: " + (a + b));
}

// Llamada de la función despues de la declaración
miFuncion(2,2);